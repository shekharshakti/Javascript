// Postfix  
let r = 2; 
s = r++; // b = 2, a = 3 
  
// Prefix 
let t = 5; 
u = ++x; // x = 6, y = 6 
  
console.log(r) 
console.log(s) 
console.log(t) 
console.log(u) 

