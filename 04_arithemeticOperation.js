// Number + Number => Addition  
let x = 1 + 2 
console.log(x) 
  
// Number + String => Concatenation 
let y =  5 + "hello" 
console.log(y) 

// Number - Number => Subtraction  
let a = 10 - 7  
console.log(a) 
  
let b = "Hello" - 1  
console.log(b) 

// Number * Number => Multiplication 
let c = 3 * 3  
let d= -4 * 4  
console.log(c) 
console.log(d) 
  
let e= Infinity * 0  
let f = Infinity * Infinity  
console.log(e) 
console.log(f) 
let z = 'hi' * 2  
console.log(z) 

// Number / Number => Division 
let g = 5 / 2 
let h = 1.0 / 2.0 
console.log(g) 
console.log(h) 
  
  
let i = 3.0 / 0 
let j = 4.0 / 0.0 
